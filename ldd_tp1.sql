drop table candidater;
drop table iut;
drop table etudiant;
 
CREATE TABLE IUT (
  NomIUT	VARCHAR2(64) PRIMARY KEY,
  Region	VARCHAR2(15),
  FI NUMBER(5)
);

CREATE TABLE ETUDIANT(
    NumEtu NUMBER(4) PRIMARY KEY,
    NomEtu VARCHAR2(10),
    Moy NUMBER(4,2),
    TailleLycee VARCHAR2(5) 
);

CREATE TABLE CANDIDATER(
    NumEtu NUMBER(3),
	NomIUT VARCHAR2(64),
    Dept VARCHAR2(5),
    Decision VARCHAR2(2),
	FOREIGN KEY (NumEtu) REFERENCES ETUDIANT(NumEtu),
	FOREIGN KEY (NomIUT) REFERENCES IUT(NomIUT) 
);

